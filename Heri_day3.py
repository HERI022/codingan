print("===", "Operator Aritmatika Menggunakan Input Dari User", "===")

angka1 = int(input("masukkan angka pertama :"))
angka2 = int(input("masukkan angka kedua :"))

#penjumlahan
hasil = angka1 + angka2
print("Hasil dari", angka1, "+", angka2, "=", hasil)

#pengurangan
hasil = angka1 - angka2
print("Hasil dari", angka1, "-", angka2, "=", hasil)

#perkalian
hasil = angka1 * angka2
print("Hasil dari", angka1, "*", angka2, "=", hasil)

#pembagian
hasil = angka1 / angka2
print("Hasil dari", angka1, "/", angka2, "=", hasil)




